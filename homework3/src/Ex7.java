public class Ex7 {

    public static void main(String[] args){
        int intVar = 12;
        double doubleVar = 10.2;

        System.out.println("int + double: " + (intVar + doubleVar));
        System.out.println("int - double: " + (intVar - doubleVar));
        System.out.println("int / double: " + (intVar / doubleVar));
        System.out.println("int * double: " + (intVar * doubleVar));
        System.out.println("int % double: " + (intVar % doubleVar));
    }
}
