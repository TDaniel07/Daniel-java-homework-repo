public class Ex3 {
    public static void main(String[] argv){
        int nr1 = 12, nr2 = 5;

        System.out.println("nr1 + nr2 = " + (nr1 + nr2));
        System.out.println("nr1 - nr2 = " + (nr1 - nr2));
        System.out.println("nr1 * nr2 = " + nr1 * nr2);
        System.out.println("nr1 / nr2 = " + 1.0 * nr1 / nr2);
        System.out.println("nr1 % nr2 = " + nr1 % nr2);
    }
}
